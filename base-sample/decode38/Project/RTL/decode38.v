module decode38 ( 
              //input 
              data_in,
              EN     ,
              
              //output 
              Y
              );

//input ports
input  [2:0]                   data_in ;      
input                          EN      ;  

//output ports
output [7:0]                   Y       ;

//reg define 
reg    [7:0]                   Y       ;
//wire define 

//parameter define 


/*******************************************************************************************************
**                              Main Program    
**  
********************************************************************************************************/
always @(data_in or EN ) begin
    if (EN == 1)
		    case (data_in )
				    3'b000: Y = 8'b11111110;
				    3'b001: Y = 8'b11111101;
				    3'b010: Y = 8'b11111011;
				    3'b011: Y = 8'b11110111;
				    3'b100: Y = 8'b11101111;
				    3'b101: Y = 8'b11011111;
				    3'b110: Y = 8'b10111111;
				    3'b111: Y = 8'b01111111;
				    default:Y = 8'bxxxxxxxx;
			endcase
    else
		        	      Y = 8'b11111111;
end

endmodule
//end of RTL code                       